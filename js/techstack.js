window.renderTechStack = function (containerId, stackList) {
  console.log('renderTechStack triggered');
  const container = document.getElementById(containerId);
  if (!container) return;

  container.innerHTML = stackList.map(key => {
    const tech = window.techIcons[key];
    if (!tech) return '';
    return `
      <div style="
        flex: 1 1 120px; 
        display: flex; 
        align-items: center; 
        justify-content: center;
      ">
        <a href="${tech.url}" target="_blank">
          <img 
            src="${tech.icon}" 
            alt="${tech.text}" 
            style="width: 80px; height: 80px; object-fit: contain;"
          />
        </a>
      </div>
    `;
  }).join('');
};